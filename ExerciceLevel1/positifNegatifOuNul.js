
/**
 * vérifie si un nombre est négatif, positif ou nul
 * @param {*} nombre nombre à vérifier
 */
function positifNegatifOuNul(nombre){
        if (nombre<0)
        {
            console.log("Nombre négatif");
        }
        else if (nombre>0)
        {
            console.log("Nombre positif");
        }
        else
        {
            console.log("Nombre nul");
        }
}